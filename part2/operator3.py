# 숫자 처리 함수

# 1) abs : 절대값
print(abs(-5)) # 5

# 2) pow : 제곱
print(pow(4, 2)) # 4^2 = 4*4 = 16

# 3) max : 최대값 
print(max(5, 12)) # 12
print(max(5, 12, 18, 27)) # 27

# 4) min : 최소값 
print(min(5, 12)) # 5
print(min(5, 12, 18, 27)) # 5

# 5) round : 반올림
print(round(3.14)) # 3
print(round(4.99)) # 5

# 6) math 라이브러리
from math import *

# floor : 내림
print(floor(4.99)) # 4

# ceil : 올림
print(ceil(3.14)) # 4

# sqrt : 제곱근
print(sqrt(25)) # 제곱근 5
