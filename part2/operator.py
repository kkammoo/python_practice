print(1+1)
print(3-2)
print(5*2)
print(6/3)

print(2**3) # ** : 제곱. 2의 3제곱 값인 8이 출력된다. 
print(5%3) # 5를 3으로 나눴을 때의 나머지 2 출력
print(10%3)
print(5//3) # // : 나눗셈의 몫. 5를 3으로 나눴을 때의 몫인 1이 출력된다.
print(10//3) # 3 출력

# 비교 연산자 (자바랑 같으니 패스~)
print(10 > 3)
print(4 >= 7)
print(10 < 3)
print(5 <= 5)

print(3 == 3)
print(4 == 2)
print(3 + 4 == 7)

print(1 != 3)
print(not (1 != 3))
print((3 > 0) and (3 < 5))
print((3 > 0) & (3 < 5)) # 파이썬에서 and 기호는 & 한 개만 사용!
print(5 > 3 > 0) # and 조건의 수식은 하나로 합칠 수 있음

print((3 > 0) or (3 > 5))
print((3 > 0) | (3 > 5)) # 파이썬에서 or 기호는 | 한 개만 사용!
